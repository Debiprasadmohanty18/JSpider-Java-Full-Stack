public class MethodSyntax {

    public static void m1(int i)
    {
        //Body...    
    }

    /*
        void m1() --> Method Signature

        public static void m1() --> Method Declaration
        {
                ---> Method Body
        }

        m1(); --> Method Calling

        m1(int i,int j);  ---> int i, int j are Formal Arguments
        
        m1(5,6); --> 5,6 are Actual Arguments

     */
}
